package com.zgqf.house.entity;

public class HousePicture {
    private Integer hp_house_id;
    private Integer hp_picture_number;
    private String hp_picture_path;

    // Getters and Setters
    public Integer getHp_house_id() {
        return hp_house_id;
    }

    public void setHp_house_id(Integer hp_house_id) {
        this.hp_house_id = hp_house_id;
    }

    public Integer getHp_picture_number() {
        return hp_picture_number;
    }

    public void setHp_picture_number(Integer hp_picture_number) {
        this.hp_picture_number = hp_picture_number;
    }

    public String getHp_picture_path() {
        return hp_picture_path;
    }

    public void setHp_picture_path(String hp_picture_path) {
        this.hp_picture_path = hp_picture_path;
    }
}
