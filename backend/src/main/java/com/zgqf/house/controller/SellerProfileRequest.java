package com.zgqf.house.controller;

public class SellerProfileRequest {
    private String s_name;
    private String s_describe;
    private String s_phone;
    private String s_email;
    private String s_website;

    // Getters and Setters
    public String getS_name() {
        return s_name;
    }

    public void setS_name(String s_name) {
        this.s_name = s_name;
    }

    public String getS_describe() {
        return s_describe;
    }

    public void setS_describe(String s_describe) {
        this.s_describe = s_describe;
    }

    public String getS_phone() {
        return s_phone;
    }

    public void setS_phone(String s_phone) {
        this.s_phone = s_phone;
    }

    public String getS_email() {
        return s_email;
    }

    public void setS_email(String s_email) {
        this.s_email = s_email;
    }

    public String getS_website() {
        return s_website;
    }

    public void setS_website(String s_website) {
        this.s_website = s_website;
    }
}
